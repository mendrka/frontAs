export const THEME_INDEX = Object.entries(THEMES).flatMap(([level, items]) =>
  items.map((theme) => ({ ...theme, level }))
)

export function getThemesForLevel(level) {
  return THEMES[level] || []
}

export function findThemeById(themeId) {
  return THEME_INDEX.find((theme) => theme.id === themeId) || null
}
